import crypto from "node:crypto";
import fs from "node:fs";
import path from "node:path";
import process from "node:process";

const args = process.argv.slice(2);

const getArgValue = (name) => {
	const index = args.indexOf(name);
	if (index === -1) return "";

	return args[index + 1] ?? "";
};

const hasFlag = (name) => args.includes(name);

const usage = `用法：
  pnpm deploy:init --app-url https://resume.example.com [--output .env.production] [--force]

说明：
  --app-url  必填，正式 HTTPS 域名，不能带路径。
  --output   可选，默认写入 .env.production。
  --force    可选，允许覆盖已存在的输出文件。`;

const appUrl = getArgValue("--app-url");
const outputPath = path.resolve(getArgValue("--output") || ".env.production");
const force = hasFlag("--force");

if (!appUrl) {
	console.error(`缺少 --app-url。\n\n${usage}`);
	process.exit(1);
}

let parsedUrl;
try {
	parsedUrl = new URL(appUrl);
} catch {
	console.error(`--app-url 不是有效 URL：${appUrl}`);
	process.exit(1);
}

if (parsedUrl.protocol !== "https:" || parsedUrl.pathname !== "/" || parsedUrl.search || parsedUrl.hash) {
	console.error("--app-url 必须是 HTTPS 站点根地址，例如 https://resume.example.com");
	process.exit(1);
}

if (fs.existsSync(outputPath) && !force) {
	console.error(`输出文件已存在：${outputPath}\n如需覆盖，请加 --force。`);
	process.exit(1);
}

const randomHex = (bytes = 32) => crypto.randomBytes(bytes).toString("hex");
const randomToken = (bytes = 24) => crypto.randomBytes(bytes).toString("base64url");

const postgresPassword = randomToken(30);
const s3AccessKey = `ruili_${randomToken(16)}`;
const s3SecretKey = randomToken(40);

const composeEnvFile = path.relative(process.cwd(), outputPath) || outputPath;

const env = `# Generated by pnpm deploy:init on ${new Date().toISOString()}
# Review SMTP/OAuth/OCR settings before opening the site to real users.

COMPOSE_ENV_FILE="${composeEnvFile.replaceAll("\\", "/")}"
PORT="3000"
SERVER_PORT="3001"
APP_URL="${parsedUrl.origin}"

POSTGRES_DB="postgres"
POSTGRES_USER="postgres"
POSTGRES_PASSWORD="${postgresPassword}"
DATABASE_URL="postgresql://postgres:${encodeURIComponent(postgresPassword)}@postgres:5432/postgres"

AUTH_SECRET="${randomHex(32)}"
ENCRYPTION_SECRET="${randomHex(32)}"
BETTER_AUTH_API_KEY=""

GOOGLE_CLIENT_ID=""
GOOGLE_CLIENT_SECRET=""
GITHUB_CLIENT_ID=""
GITHUB_CLIENT_SECRET=""
LINKEDIN_CLIENT_ID=""
LINKEDIN_CLIENT_SECRET=""

OAUTH_PROVIDER_NAME=""
OAUTH_CLIENT_ID=""
OAUTH_CLIENT_SECRET=""
OAUTH_DISCOVERY_URL=""
OAUTH_AUTHORIZATION_URL=""
OAUTH_TOKEN_URL=""
OAUTH_USER_INFO_URL=""
OAUTH_SCOPES="openid profile email"

SMTP_HOST=""
SMTP_PORT=""
SMTP_USER=""
SMTP_PASS=""
SMTP_FROM="锐历 <noreply@${parsedUrl.hostname}>"
SMTP_SECURE="false"

APP_MEMORY_LIMIT="900m"
APP_SHM_SIZE="256m"

S3_ACCESS_KEY_ID="${s3AccessKey}"
S3_SECRET_ACCESS_KEY="${s3SecretKey}"
S3_REGION="us-east-1"
S3_ENDPOINT="http://seaweedfs:8333"
S3_BUCKET="reactive-resume"
S3_FORCE_PATH_STYLE="true"

REDIS_URL="redis://redis:6379"

OCR_PROVIDER=""
OCR_AZURE_DOCUMENT_INTELLIGENCE_ENDPOINT=""
OCR_AZURE_DOCUMENT_INTELLIGENCE_KEY=""
OCR_MIN_TEXT_CHARS="120"
OCR_TIMEOUT_MS="45000"
OCR_POLL_INTERVAL_MS="1000"

FLAG_DISABLE_SIGNUPS="false"
FLAG_DISABLE_EMAIL_AUTH="false"
FLAG_DISABLE_IMAGE_PROCESSING="false"
FLAG_TRUST_PROXY_HEADERS="false"
FLAG_ALLOW_UNSAFE_OAUTH_REDIRECT_URI="false"
FLAG_ALLOW_UNSAFE_AI_BASE_URL="false"

GOOGLE_CLOUD_API_KEY=""
CROWDIN_PROJECT_ID=""
CROWDIN_API_TOKEN=""
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, env, { encoding: "utf8", flag: "w" });

console.log(`已生成生产环境变量：${outputPath}`);
console.log("下一步：");
console.log(`1. 检查并按需补充 SMTP/OAuth/OCR：${outputPath}`);
console.log(`2. pnpm deploy:check ${path.relative(process.cwd(), outputPath) || outputPath}`);
console.log(
	`3. docker compose -f compose.yml --env-file ${path.relative(process.cwd(), outputPath) || outputPath} config --quiet`,
);
